% پارامترها
Fs = 1000; % نرخ نمونه‌برداری (1kHz)
f = 5; % فرکانس سیگنال (5Hz)

% ایجاد سیگنال مثلثی پریودیک
t = 0:1/Fs:0.4; % بازه زمانی
triangularSignal = sawtooth(2*pi*f*t, 0.5);

% نمایش سیگنال
plot(t, triangularSignal);
title('سیگنال مثلثی پریودیک');
xlabel('زمان (ثانیه)');
ylabel('مقدار سیگنال');

